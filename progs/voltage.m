function v = voltage(V,R)
    A0 = [(-R(3)*R(8)*R(4)) , (R(8)*R(7)*R(4)+R(3)*R(7)*R(4)+R(3)*R(8)*R(4)+R(3)*R(7)*R(8)) , (-R(3)*R(7)*R(4))]; 
    A1 = [(R(7)*R(2)+R(1)*R(2)+R(1)*R(7)) , (-R(1)*R(2)) , (0)];
    A2 = [(0) , (-R(5)*R(6)) , (R(5)*R(8)+R(5)*R(6)+R(8)*R(6))];
    A = [A0;A1;A2];
    B = [V*R(8)*R(7)*R(4) ; V*R(7)*R(2) ; V*R(6)*R(8) ];
    v = A\B;
end